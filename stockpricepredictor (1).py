# Create a function to get the sentiment scores
def getSIA(text):
  # pre trianed to recognize sentiment laden words and phrases
  sia = SentimentIntensityAnalyzer()
  # calculate sentiment scores for the input text and return:
  # compound - overall sentiment score (-1 to 1 -> most negative to most positive)
  # neg - proportion of negative sentiment in the text
  # neu
  # pos
  sentiment = sia.polarity_scores(text)
  return sentiment # dictionary containing sentiment score

