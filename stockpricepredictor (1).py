# Create function to get the subjectivity
def getSubjectivity(text):
  # TextBlob provides simple APIs for diving into common NLP tasks
  # sentiment properties from TextBlob object
  # subjectivities score from the sentiment properties
  return TextBlob(text).sentiment.subjectivity


# Create function to get the polarity
def getPolarity(text):
  # Measure the positive/negativity of the text
  # Helps determine the overall sentiment of posts content
  return TextBlob(text).sentiment.polarity

